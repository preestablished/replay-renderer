#[tokio::test]
async fn mp4_ffprobe_contract() {
    let dir = TestDir::new("mp4_ffprobe_contract");
    let out = dir.path("contract.mp4");

    let table = probe_encoders("ffmpeg", true).await; // force_software
    assert_eq!(table.h264, EncoderChoice::Libx264);

    let o = video_options(18);
    let g = geometry(1024, 896);
    let args = mp4_args(&o, &g, table.h264, "determinism:test-job:deadbeef", &out);
    let mut session = EncoderSession::spawn("ffmpeg", &args)
        .await
        .expect("spawn ffmpeg");
    for native in load_fixture_rgb24(usize::MAX) {
        let up = pad_to_even(&scale_nn(&native, 4));
        assert_eq!((up.width, up.height), (1024, 896));
        session.write_frame(&up.pixels).await.expect("write frame");
    }
    session.finish().await.expect("ffmpeg finishes cleanly");

    let probe = tokio::process::Command::new("ffprobe")
        .args([
            "-v",
            "error",
            "-select_streams",
            "v:0",
            "-show_entries",
            "stream=codec_name,width,height,r_frame_rate,nb_frames",
            "-of",
            "json",
            &out,
        ])
        .output()
        .await
        .expect("run ffprobe");
    assert!(
        probe.status.success(),
        "ffprobe failed: {}",
        String::from_utf8_lossy(&probe.stderr)
    );
    let v: serde_json::Value = serde_json::from_slice(&probe.stdout).expect("ffprobe json");
    let s = &v["streams"][0];
    assert_eq!(s["codec_name"], "h264");
    assert_eq!(s["width"], 1024);
    assert_eq!(s["height"], 896);
    assert_eq!(s["nb_frames"], "600");
    // 6010/100 reduced by ffmpeg.
    assert_eq!(s["r_frame_rate"], "601/10");
}

#[tokio::test]
async fn mp4_decode_back_mean_abs_error_below_3() {
    let dir = TestDir::new("mp4_decode_back");
    let out = dir.path("mae.mp4");

    // 120-frame subset at ×2 keeps runtime and memory sane; NEVER
    // golden-compare encoded bitstreams (encoder builds differ).
    let frames: Vec<Rgb24Frame> = load_fixture_rgb24(120)
        .iter()
        .map(|f| pad_to_even(&scale_nn(f, 2)))
        .collect();
    let (w, h) = (frames[0].width, frames[0].height);

    let o = video_options(18);
    let g = geometry(w, h);
    let args = mp4_args(&o, &g, EncoderChoice::Libx264, "determinism:test:mae", &out);
    run_with_frames("ffmpeg", &args, frames.iter().map(|f| f.pixels.as_slice()))
        .await
        .expect("encode");

    let decoded = run_capture_stdout("ffmpeg", &decode_to_rawvideo_args(&out, w, h), &[])
        .await
        .expect("decode back");
    let frame_bytes = 3 * w as usize * h as usize;
    assert_eq!(
        decoded.len(),
        frame_bytes * frames.len(),
        "decoded frame count/size"
    );

    let mut sum: u64 = 0;
    for (i, frame) in frames.iter().enumerate() {
        let d = &decoded[i * frame_bytes..(i + 1) * frame_bytes];
        for (a, b) in frame.pixels.iter().zip(d) {
            sum += u64::from(a.abs_diff(*b));
        }
    }
    let mae = sum as f64 / (frame_bytes * frames.len()) as f64;
    assert!(mae < 3.0, "mean absolute error {mae} >= 3.0");
}

