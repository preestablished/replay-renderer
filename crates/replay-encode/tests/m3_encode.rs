// ---- GIF ----------------------------------------------------------------

#[tokio::test]
async fn gif_respects_max_frames_and_dither_none() {
    // dither=none is normative (§7.4).
    let g = geometry(256, 224);
    let args = gif_encode_args(&g, "palette.png", "out.gif");
    assert!(
        args.iter().any(|a| a.contains("paletteuse=dither=none")),
        "args: {args:?}"
    );

    // Range limit: > max refused with a typed error.
    assert!(check_gif_range(900, GIF_DEFAULT_MAX_FRAMES).is_ok());
    match check_gif_range(901, GIF_DEFAULT_MAX_FRAMES) {
        Err(EncodeError::GifRangeExceeded {
            frame_count: 901,
            max_frames: 900,
        }) => {}
        other => panic!("expected GifRangeExceeded, got {other:?}"),
    }

    // Two-pass encode of a small GIF actually works.
    let dir = TestDir::new("gif_two_pass");
    let palette = dir.path("palette.png");
    let out = dir.path("out.gif");
    let frames: Vec<Rgb24Frame> = (0..60).map(|i| synthetic_frame(i, 256, 224)).collect();
    run_with_frames(
        "ffmpeg",
        &gif_palettegen_args(&g, &palette),
        frames.iter().map(|f| f.pixels.as_slice()),
    )
    .await
    .expect("palettegen pass");
    run_with_frames(
        "ffmpeg",
        &gif_encode_args(&g, &palette, &out),
        frames.iter().map(|f| f.pixels.as_slice()),
    )
    .await
    .expect("gif encode pass");
    let meta = std::fs::metadata(&out).expect("gif exists");
    assert!(meta.len() > 0, "gif is empty");
}

// ---- WebP ----------------------------------------------------------------

#[tokio::test]
async fn webp_lossless_round_trips_byte_identical() {
    let dir = TestDir::new("webp_lossless");
    let out = dir.path("out.webp");
    let (w, h) = (256, 224);
    let g = geometry(w, h);
    let frames: Vec<Rgb24Frame> = (0..30).map(|i| synthetic_frame(i, w, h)).collect();

    run_with_frames(
        "ffmpeg",
        &webp_args(&g, &out),
        frames.iter().map(|f| f.pixels.as_slice()),
    )
    .await
    .expect("webp encode");

    // Decode back via ffmpeg. ffmpeg's native webp decoder cannot demux
    // animated WebP (it skips ANIM/ANMF chunks — a long-standing gap), so
    // split the container: each ANMF frame's lossless VP8L payload is
    // rewrapped as a static WebP, which ffmpeg decodes exactly. Lossless
    // means byte-identical RGB.
    let container = std::fs::read(&out).expect("webp exists");
    let stills = split_animated_webp(&container, w, h);
    assert_eq!(stills.len(), frames.len(), "ANMF frame count");
    let frame_bytes = 3 * w as usize * h as usize;
    for (i, (still, src)) in stills.iter().zip(&frames).enumerate() {
        let still_path = dir.path(&format!("frame_{i}.webp"));
        std::fs::write(&still_path, still).expect("write still webp");
        let decoded =
            run_capture_stdout("ffmpeg", &decode_to_rawvideo_args(&still_path, w, h), &[])
                .await
                .expect("decode still webp frame");
        assert_eq!(decoded.len(), frame_bytes, "decoded frame {i} size");
        assert_eq!(
            decoded, src.pixels,
            "webp lossless frame {i} is not byte-identical"
        );
    }
}

/// Split an animated WebP into per-frame static WebP files (VP8L payloads
/// rewrapped in a minimal RIFF container). Asserts every ANMF frame is a
/// full-canvas lossless frame so the comparison is well-defined.
fn split_animated_webp(webp: &[u8], canvas_w: u32, canvas_h: u32) -> Vec<Vec<u8>> {
    let le24 = |b: &[u8]| u32::from(b[0]) | u32::from(b[1]) << 8 | u32::from(b[2]) << 16;
    assert_eq!(&webp[0..4], b"RIFF", "not a RIFF file");
    assert_eq!(&webp[8..12], b"WEBP", "not a WebP file");
    let mut stills = Vec::new();
    let mut off = 12;
    while off + 8 <= webp.len() {
        let fourcc = &webp[off..off + 4];
        let size = u32::from_le_bytes(webp[off + 4..off + 8].try_into().unwrap()) as usize;
        let payload = &webp[off + 8..off + 8 + size];
        if fourcc == b"ANMF" {
            // 16-byte ANMF header: x/2, y/2, w-1, h-1 (24-bit LE each),
            // duration, flags — then the frame subchunk.
            assert_eq!(le24(&payload[0..3]), 0, "frame x offset");
            assert_eq!(le24(&payload[3..6]), 0, "frame y offset");
            assert_eq!(le24(&payload[6..9]) + 1, canvas_w, "frame width");
            assert_eq!(le24(&payload[9..12]) + 1, canvas_h, "frame height");
            let sub = &payload[16..];
            assert_eq!(&sub[0..4], b"VP8L", "expected a lossless frame chunk");
            let vp8l_len = u32::from_le_bytes(sub[4..8].try_into().unwrap()) as usize;
            let vp8l = &sub[8..8 + vp8l_len];

            let mut still = Vec::with_capacity(20 + vp8l.len() + 1);
            still.extend_from_slice(b"RIFF");
            let chunk_len = 8 + vp8l.len() + (vp8l.len() & 1);
            still.extend_from_slice(&((4 + chunk_len) as u32).to_le_bytes());
            still.extend_from_slice(b"WEBP");
            still.extend_from_slice(b"VP8L");
            still.extend_from_slice(&(vp8l_len as u32).to_le_bytes());
            still.extend_from_slice(vp8l);
            if vp8l.len() & 1 == 1 {
                still.push(0);
            }
            stills.push(still);
        }
        off += 8 + size + (size & 1);
    }
    stills
}

// ---- stills determinism ---------------------------------------------------

#[test]
fn stills_are_deterministic() {
    let frames: Vec<Rgb24Frame> = (0..37).map(|i| synthetic_frame(i, 64, 56)).collect();
    let pairs: Vec<(u64, &Rgb24Frame)> = frames
        .iter()
        .enumerate()
        .map(|(i, f)| (i as u64 * 3, f))
        .collect();

    let sheet_a = contact_sheet(&pairs);
    let sheet_b = contact_sheet(&pairs);
    assert!(sheet_a.width > 0 && sheet_a.height > 0);
    assert_eq!(sheet_a, sheet_b, "contact_sheet is not deterministic");

    let strip_a = thumb_strip(&pairs);
    let strip_b = thumb_strip(&pairs);
    assert!(strip_a.width > 0 && strip_a.height > 0);
    assert_eq!(strip_a, strip_b, "thumb_strip is not deterministic");
}
